    public void showJWindow(Component owner) {
        if (jw == null) {
            jw = new JWindow();
            jw.add(this);
            jw.setSize(this.getPreferredSize());
            jw.setLocationRelativeTo(owner);
        }
        checkFields();
        jw.setVisible(true);
    }

